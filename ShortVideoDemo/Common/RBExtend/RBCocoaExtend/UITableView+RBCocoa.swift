import Foundation
import Foundation
import UIKit

extension UITableView {
    
}

extension UITableView {
    
    @discardableResult
    override func frameSet(_ frame: CGRect) -> UITableView {
        return super.frameSet(frame) as! UITableView
    }
    
    @discardableResult
    override func xSet(_ x: CGFloat) -> UITableView {
        return super.xSet(x) as! UITableView
    }
    
    @discardableResult
    override func ySet(_ y: CGFloat) -> UITableView {
        return super.ySet(y) as! UITableView
    }
    
    @discardableResult
    override func widthSet(_ width: CGFloat) -> UITableView {
        return super.widthSet(width) as! UITableView
    }
    
    @discardableResult
    override func heightSet(_ height: CGFloat) -> UITableView {
        return super.heightSet(height) as! UITableView
    }
    
    @discardableResult
    override func sizeSet(_ size: CGSize) -> UITableView {
        return super.sizeSet(size) as! UITableView
    }
    
    @discardableResult
    override func centerSet(_ center: CGPoint) -> UITableView {
        return super.centerSet(center) as! UITableView
    }
    
    @discardableResult
    override func centerXSet(_ x: CGFloat) -> UITableView {
        return super.centerXSet(x) as! UITableView
    }
    
    @discardableResult
    override func centerYSet(_ y: CGFloat) -> UITableView {
        return super.centerYSet(y) as! UITableView
    }
    
    @discardableResult
    override func backgroundColorSet(_ color: UIColor) -> UITableView {
        return super.backgroundColorSet(color) as! UITableView
    }
    
    @discardableResult
    override func alphaSet(_ alpha: CGFloat) -> UITableView {
        return super.alphaSet(alpha) as! UITableView
    }
    
    @discardableResult
    override func isHiddenSet(_ isHidden: Bool) -> UITableView {
        return super.isHiddenSet(isHidden) as! UITableView
    }
    
    @discardableResult
    override func contentModeSet(_ contentMode: UIView.ContentMode) -> UITableView {
        return super.contentModeSet(contentMode) as! UITableView
    }
    
    @discardableResult
    override func isOpaqueSet(_ isOpaque: Bool) -> UITableView {
        return super.isOpaqueSet(isOpaque) as! UITableView
    }
    
    @discardableResult
    override func clipsToBoundsSet(_ clipsToBounds: Bool) -> UITableView {
        return super.clipsToBoundsSet(clipsToBounds) as! UITableView
    }
    
    @discardableResult
    override func addTo(superView: UIView) -> UITableView {
        return super.addTo(superView: superView) as! UITableView
    }
    
}
